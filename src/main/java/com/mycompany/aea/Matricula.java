    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
float descuento = 0f;
    float pension = 520f;
    float saldo = 0f;

    // Verificar si txtAcuenta está habilitado
    if (txtAcuenta.isEnabled()) {
        try {
            // Intentar obtener el valor de txtAcuenta como un número flotante
            descuento = Float.parseFloat(txtAcuenta.getText());
        } catch (NumberFormatException e) {
            // Manejar la excepción si el texto no es un número válido
            manejarNumberFormatException("Ingrese un número válido en txtAcuenta");
            return;
        }
    }

    if (chkBeca.isSelected()) {
        // Si hay beca, la pensión es cero
        pension = 0f;
    }

    saldo = pension - descuento;

    // Método para obtener el nivel seleccionado
    String nuevoNombreCompleto = this.txtApellido.getText() + " " + this.txtNombre.getText();
        int secc = cbSeccion.getSelectedIndex();
        String re = null;
        switch(secc){
            case 0:
                re = null;
                break;
            case 1:
                re = "A";
                break;
            case 2:
                re = "B";
                break;
            case 3:
                re = "C";
                break;
            default:
                return;
        }
    int grad = cbGrado.getSelectedIndex();
    String gd = null;
        switch(grad){
            case 0:
                gd = null;
                break;
            case 1:
                gd = "1er Grado";
                break;
            case 2:
                gd = "2do Grado";
                break;
            case 3:
                gd = "3er Grado";
                break;
            case 4:
                gd = "4to Grado";
                break;
            case 5:
                gd = "5to Grado";
                break;
            default:
                return;
        }

    // Verificar si el nombre y apellido ya están en la lista
    if (!lista.contains(nuevoNombreCompleto)) {
        // El nombre y apellido no están en la lista, agregarlos
        lista.addElement(nuevoNombreCompleto);
        Saldo.addElement("S/." + saldo);
        Pension.addElement("S/." + pension);
        Acuenta.addElement("S/." + descuento);
        codigo.addElement(generarCorrelativo() + " - ");
        seccion.addElement(re);
        grado.addElement(gd);
        // Incrementar el conteo de alumnos
        totalAlumnos++;
        totalPension += pension;
        totalDescuento += descuento;
        totalSaldo += saldo;

        // Actualizar el campo de texto con el total
        actualizarTotales();

        // Establecer modelos en las listas
        this.lstname.setModel(lista);

        // Habilitar o deshabilitar el botón "Quitar" según si la lista está vacía
        btnQuitar.setEnabled(!lista.isEmpty());

    } else {
        // El nombre y apellido ya están en la lista
        // Mostrar un mensaje de error con JOptionPane
        JOptionPane.showMessageDialog(null, "Error: Nombre y apellido repetidos", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Establecer modelos en las demás listas
    this.lstSaldo.setModel(Saldo);
    this.lstPension.setModel(Pension);
    this.lstDscto.setModel(Acuenta);
    this.lstCodigo.setModel(codigo);
    this.lstSeccion.setModel(seccion);
    this.lstGrado.setModel(grado);
    

    // Obtener la especialidad seleccionada
    String especialidad = obtenerEspecialidadSeleccionada();

    // Mostrar la especialidad en lstHabilidad
    
    habilidad.addElement(especialidad);
    this.lstHabilidad.setModel(habilidad);
    lstHabilidad.setVisible(true);

    // Habilitar o deshabilitar el botón "Quitar" según si la lista está vacía
    btnQuitar.setEnabled(!lista.isEmpty());
            
    }//GEN-LAST:event_btnIngresarActionPerformed

   private void manejarNumberFormatException(String mensajeError) {
    System.err.println("Error: " + mensajeError);
    JOptionPane.showMessageDialog(this, mensajeError, "Error", JOptionPane.ERROR_MESSAGE);
    txtAcuenta.setText("");
}

private void actualizarTotales() {
    this.txtSaldo.setText(Integer.toString(totalSaldo));
    this.txtDscto.setText(Integer.toString(totalDescuento));
    this.txtTotalAlumnos.setText(Integer.toString(totalAlumnos));
    this.txtPension.setText(Integer.toString(totalPension));
}

private String obtenerEspecialidadSeleccionada() {
    int especialidadIndex = cbHabilidad.getSelectedIndex();
    String especialidad = null;

    switch (especialidadIndex) {
        case 0:
            especialidad = "Seleccionar habilidad";
            break;
        case 1:
            especialidad = "Arte";
            break;
        case 2:
            especialidad = "Deporte";
            break;
        case 3:
            especialidad = "Razonamiento Matematico";
            break;
    }

    return especialidad;
} 
    
    private String generarCorrelativo() {
    // Incrementar el número correlativo y formatear para tener dos dígitos
    numeroCorrelativo = (numeroCorrelativo % 25) + 1; // Reinicia si llega a 99
    return String.format("%02d", numeroCorrelativo);
}
    
