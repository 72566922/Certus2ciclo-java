    private void txtDNIKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDNIKeyReleased
        // TODO add your handling code here:
     String texto = txtDNI.getText();
    
    // Limpiar el campo de cualquier caracter no numérico
    StringBuilder sb = new StringBuilder();
    for (char c : texto.toCharArray()) {
        if (Character.isDigit(c)) {
            sb.append(c);
        } else {
            // Mostrar un mensaje de error
            JOptionPane.showMessageDialog(this, "Por favor, ingrese solo números.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    // Establecer el texto limpio en el campo
    txtDNI.setText(sb.toString());
    
    // Llamar a la función de verificación de campos
    verificarCampos();
    }//GEN-LAST:event_txtDNIKeyReleased

